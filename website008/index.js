// String methods , So many ways to manipulate your string

let username = "Lucas"

console.log(username.charAt(0))

console.log(username.indexOf("u"))

console.log(username.lastIndexOf("u"))

console.log(username.length)

console.log(username.trim())

console.log(username.toLowerCase())

console.log(username.toUpperCase())

console.log(username.replaceAll("u", "o"))

console.log(username.replace("u", "o"))

console.log(username.startsWith("u"))

console.log(username.endsWith("s"))

console.log(username.includes("u"))

console.log(username.repeat(3))

console.log(username.padStart(20, "0"))

console.log(username.padEnd(20, "0"))